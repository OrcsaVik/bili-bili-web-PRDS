package com.github.bilibili.comments.mq;

import org.springframework.amqp.rabbit.core.RabbitTemplate;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Component;

@Component
public class RabbitMQSender {

    @Autowired
    private final RabbitTemplate rabbitTemplate;

    // Exchange defined in infrastructure config
    private static final String EXCHANGE_NAME = "moments.exchange";
    private static final String ROUTING_KEY = "moments.created";

    public RabbitMQSender(RabbitTemplate rabbitTemplate) {
        this.rabbitTemplate = rabbitTemplate;
    }

    public void sendMomentEvent(MomentCreatedEvent event) {
        // Sends the lightweight event to the exchange
        rabbitTemplate.convertAndSend(EXCHANGE_NAME, ROUTING_KEY, event);
    }
}
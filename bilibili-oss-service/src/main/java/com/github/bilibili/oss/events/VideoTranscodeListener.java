package com.github.bilibili.oss.events;

import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.context.event.EventListener;
import org.springframework.scheduling.annotation.Async;
import org.springframework.stereotype.Component;

@Component
@RequiredArgsConstructor
@Slf4j
public class VideoTranscodeListener {

    private final VideoTranscodeTask worker;

    /*
     * @Async is mandatory here. Without it, your HTTP request hangs while FFmpeg churns for 10 minutes. That is unacceptable latency.
     */
    @Async
    @EventListener
    public void onVideoUploaded(VideoUploadedEvent event) {
        log.debug("Event received for video: {}", event.getVideoId());
        worker.processVideo(event.getVideoId(), event.getObjectKey());
    }
}